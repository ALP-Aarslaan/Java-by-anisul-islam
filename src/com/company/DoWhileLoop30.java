package com.company;

public class DoWhileLoop30 {
    public static void main(String[] args) {
        /*
        syntax of do while loop:
        initialization ;
        do{
        statements;
        increment / decrement;
        }while(condition);

         */
        int i=1;
        do{
            System.out.println(i+" BANGLADESH");
            i=i+2;
        }while(i<=100);

    }
}
